package tetris.se.liu.ida.antni450.tetris;

/**
 * enum for the different square types
 */
public enum SquareType
{
    //These names are too short but they are still very representative of what they are
    /**
     * Square type for an I-block
     */
    I,
    /**
     * Square type for an O-block
     */
    O,
    /**
     * Square type for a T-block
     */
    T,
    /**
     * Square type for a S-block
     */
    S,
    /**
     * Square type for a Z-block
     */
    Z,
    /**
     * Square type for a J-block
     */
    J,
    /**
     * Square type for an L-block
     */
    L,
    /**
     * Square type for an EMPTY-block
     */
    EMPTY,
    /**
     * Square type for an OUTSIDE-block
     */
    OUTSIDE
}
